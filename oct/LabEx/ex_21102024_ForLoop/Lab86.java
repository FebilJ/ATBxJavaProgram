package oct.LabEx.ex_21102024_ForLoop;

public class Lab86 {
	public static void main(String args[]) {
		for(int i = 0; i < 10; i++) { //10
			System.out.println(i);
			//i -> 0, 9 - 10 times
		}
		
		for(int a = 1; a <= 10; a++) {  //10
			System.out.println(a);
			//a -> 1 to 10, -> 10 times
		}
	}
}
